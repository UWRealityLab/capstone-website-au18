            <h3>Deliverables and Tasks</h3>
            </div>
            <hr class="underliner" />
            <p><h4>Initial Idea Online Post: Due Wednesday, October 10, Noon PT</h4></p>
            <p>With your team, post on <a href="https://groups.google.com/a/cs.washington.edu/forum/#!forum/cse481v-18au-discussion">Google Groups</a> with the following information:</p>
            <ul><li>Team Name (be creative!)</li>
            <li>Project Summary (1-2 sentences)</li>
            <li>Project Description (2-3 paragraphs)</li>
            <li>Vision for your demo (what would people experience if they try your demo</li>
            <li>Hardware Platform/Device (Acer VR Headset, Hololens, Magic Leap, ZenfoneAR)</li>
            <li>Extra Resources you will need (hardware, assets etc.) -- can be modified later</li>
            <li>Approximate timeline (plan for each week)</li>
            <li>Finally, questions about feasibility, platform etc.</li>
            </ul>

            <p><h4>Initial Idea Pitch: In-Lab Thursday, October 11</h4></p>
            <ul><li>10 minute pitch about your project, with more details</li>
            <li>You can use slides, visuals, storyboards, etc.</li>
            <li>Get feedback on your projects from course staff</li>
            </ul>

            <p><h4>Project Webpage for each team:</h4></p>
            <p>Each team is required to create a webpage that will include all the material created throughout the quarter. Every deliverable will be linked from your webpage. Teams can use any hosting service they like. The style should be like on Kickstarter, (<u><a href="https://www.kickstarter.com/projects/scanse/sweep-scanning-lidar?ref=category" target="_blank">Example</a></u>). Keep the website simple so it easy for us to navigate.</p>
            <p>Make sure to document everything and put it on the website (notes, pictures, videos, etc), and send us the link that we can add to the course webpage.</p>
            <p>Please setup your website by <strong>Thursday, Oct 18, 23:59 (11:59pm)</strong>, and send a link to vr-capstone-staff@cs. Initially, the website can contain a your initial project proposals (+any slides you used, &amp; incorporating feedback/updates) and a short bio/background of the team members. You can update and improve your website as the quarter progresses.</p>

            This website will eventually have several components:
                    <ul>
                        <li>Quick description of your project</li>
                        <li>A short bio/background of the team members</li>
                        <li>A link to your PRD </li>
                        <li>A hype/demo video (later on in the quarter)</li>
                        <li>Weekly updates on progress as blog posts</li>
                    </ul>

            <p>Here are some example (final) project websites from previous years:</p>
            <ul>
            <li><a href="http://sonjakhan.github.io/vrgrads/">Website Example 1</a></li>
