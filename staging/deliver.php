            <p><h4>Product Requirements Document (PRD):</h4></p>
            <p>After firming up the project idea, each team will be required to create a <a href="https://en.wikipedia.org/wiki/Product_requirements_document">Product Requirements Document</a> (PRD) that details all the various aspects of your project. It is written to allow stakeholders to understand what a product should do, and also allow serve as a guiding document for the team that is building the product. </p>
            <p>PRD should include a one paragraph summary of the project (summarize your project proposal), deliverables (what are you going to deliver at the end), the features (ranked by priority), any performance metrics, milestones, responsibilities of each team member, materials and outside help needed, budget, risks, and how risks will be addressed. You will be asked to do this very early in the class and then update your PRD throughout the quarter. We will use the PRD to see how your end product matches your PRD, so make sure to keep it up to date. Your PRD should be on your website.</p>
            <p>PRD template: <u><a href="https://goo.gl/rZsYnD" target="_blank">https://goo.gl/rZsYnD</a></u></p>
            
            <p><h4>Weekly meeting with staff:</h4></p>
            <p>You will meet for 15 min weekly with the instructors in SIEG 327. Every Thursday between 1:30 - 4:20. You will describe your progress during the past week, present any demos, and discuss the plan for the upcoming week. The staff can also help answer any questions or resolve blocking issues.</p>

            <p><h4>Week 7 Progress report to the class:</h4></p>
            <p>Each team will do a 10 min presentation to the class during lecture time. We recommend using slides that motivate your problem, describe your current progress and include any demos/videos/sketches of your system. This is a great opportunity to gather feedback from your peers.</p>

            <p><h4>Peer reviews</h4></p>
            <p>You will be conducting two teammate group peer reviews during the quarter, around week 7 and week 11 (finals week). This will involve giving demos of your work to other teams and gathering feedback.</p>

            <p><h4>Demo Video</h4></p>
            <p>Create a video that presents the capabilities of your app and an example of how it is played or used (like the one you're going to demo during the final session). The videos are going to be played during the demo session and placed on your websites. <a href="https://www.youtube.com/playlist?list=PLSCK7y_AEdoZLOjca-f0yNCtGl1MakBWt">Examples of last year's videos.</a> The videos can be between 2-5 minutes long. Can include narration if necessary (kickstarter style) or just demonstrate technical features of your project. </p>

            <p><h4>Demo Plan and Rehersal</h4></p>
            <p>You will be required to come up with a demo plan, that includes the floor-plan layout for your space, any furniture/props and some sinage for the team project. You will also need to prepare a demo 'script' and reherse the script in the Lab, a week prior to the final demo day.</p>

            <p><h4>Demo Day</h4></p>
            <p>The final demo day will take place in the CSE atrium tentatively on Thursday, December 13th. And there will be a rehearsal, one week prior. Mark your calendars now! More details will be sent later in the quarter as they are confirmed.</p>
            <p>It will be very exciting, fun, and a fitting end to the capstone! It will be open to friends, family and general public. We will also invite a number of guests from companies like Microsoft, Magic Leap, Google, Facebook, Oculus, Valve, and more!</p>
            <p>Staff will evaluate final demos on Novelty, Difficulty, Production Value, Final Demo, Presentation</p>

            <p><h4>After the Capstone Ends</h4></p>
            <p>Your course related obligations end after demo day. However, some teams may choose to continue working on their capstone project in order to release it on the App Store, Open Source, etc. This is a great way to get your creations out into the world and gain recognition for your work! If your team would like to continue working on the project, please contact the course staff and we will be happy to help you!</p>


