            </ul>

            <p><h4>Weekly Blogs:</h4></p>
            <p>Each team is required to maintain a blog on their team webpage. You will be required to update it on a weekly basis (<strong>due Friday at 7pm</strong>). Blog should at least contain:</p>
            <ol>
            <li>Summary: Overview of the last week&#39;s activities</li>
            <li>Progress: More detail on what has been accomplished in the past week. Include any designs, screenshots, notes for that week, or interesting technical challenges/solutions that you faced.</li>
            <li>Plan for next week: Goals and execution plan for the next week, including task breakup amongst team members. Also think/write about the &#39;demo&#39; that you will show the staff in next week&#39;s lab.</li>
            </ol>
            <p>These will be graded weekly.</p>
