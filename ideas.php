                        <p><h3>Brainstorming Advice</h3></p>
                        <p>While brainstorming, first define the problem you are trying to solve. Would people want to use it? Would it bring them joy and delight? Would it improve their lives? Would it be fun?</p>

                        <p>Also think about each device category carefully, and what features it offers. e.g. Headsets are more immersive, but not as widely available. Phones are less immersive, but easy to use and almost everyone has one. It may also be possible to think of ideas that combine multiple devices. You could also imagine using extra hardware, sensors or instruments. We have the following devices available for use: <a href="https://www.acer.com/ac/en/US/content/series/wmr">Windows MR Headsets (VR)</a>, <a href="https://www.asus.com/us/Phone/ZenFone-AR-ZS571KL/">Asus Zenfone AR (Mobile AR)</a>, <a href="https://www.magicleap.com/magic-leap-one">Magic Leap One (AR Headset)</a> and <a href="https://www.microsoft.com/en-US/hololens">Hololens (AR Headset)</a></p>

                        <p>Here are some more questions to think about: Who is the intended audience? How would you know you are successful? What are current solutions or why isnâ€™t this problem already solved? What does the VR/AR medium provide that is not possible/ or easy to do through other medium?</p>

                        <p>Work in a team while brainstorming, document all ideas and iterate. Get lots of feedback from classmates/staff on the <a href="https://groups.google.com/a/cs.washington.edu/forum/#!forum/cse481v-18au-discussion">discussion board</a>. And come up with a cool team name!</p>

                        

